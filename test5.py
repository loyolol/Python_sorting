print("\nЗадание 5")

students = [
    ("Иванов", "Пётр", 4.5),
    ("Сидоров", "Алексей", 4.8),
    ("Петров", "Иван", 4.8),
    ("Смирнова", "Анна", 4.2)
]

# сортировка
students_sorted = sorted(
    students,
    key=lambda x: (-x[2], x[0])
)

top5 = students_sorted[:5]

print("Топ студентов:")

for s in top5:
    print(s[0], s[1], s[2])